export function t(locale: UiLocale, key: I18nKey, values: Record<string, string | number> = {}): string {
  return MESSAGES[locale][key].replace(/\{(\w+)\}/g, (match, name: string) => {
    return values[name] === undefined ? match : String(values[name])
  })
}

export function resolveUiLocale(preference: UiLocalePreference | undefined, browserLanguage = getBrowserLanguage()): UiLocale {
  if (preference && preference !== "auto") {
    return preference
  }

  return matchSupportedLocale(browserLanguage)
}

export function isUiLocalePreference(value: unknown): value is UiLocalePreference {
  return value === "auto" || isUiLocale(value)
}

function isUiLocale(value: unknown): value is UiLocale {
  return typeof value === "string" && SUPPORTED_UI_LOCALES.some(locale => locale.code === value)
}

function matchSupportedLocale(language: string | undefined): UiLocale {
  const normalized = language?.replace("_", "-").toLowerCase() ?? ""

  if (normalized.startsWith("zh-hant") || normalized === "zh-tw" || normalized === "zh-hk") {
    return "zh-TW"
  }
  if (normalized.startsWith("zh")) {
    return "zh-CN"
  }

  const match = SUPPORTED_UI_LOCALES.find(locale =>
    normalized === locale.code.toLowerCase() || normalized.startsWith(`${locale.code.toLowerCase()}-`),
  )

  return match?.code ?? "en"
}

function getBrowserLanguage(): string | undefined {
  try {
    return chrome.i18n?.getUILanguage?.() ?? navigator.language
  }
  catch {
    return navigator.language
  }
}
